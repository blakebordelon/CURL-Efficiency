def logisticloss(D):
    """ k-way logistic loss
    """
    return torch.log2(1 + (torch.exp(D)).squeeze(-1).sum(-1))

mysoftplus=nn.Softplus()
def softplus(D):
    """ scaled softplus
    """
    return mysoftplus(D/128)

